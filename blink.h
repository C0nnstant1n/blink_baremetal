#ifndef BLINK_H
#define BLINK_H

#include "sl_simple_led_instances.h"

// Определение экземпляра светодиода (можно переопределить извне)
#ifndef LED_INSTANCE
#define LED_INSTANCE sl_led_led0
#endif

#define LED_ON 1
#define LED_OFF 0

/**
 * @brief Функция обработки мигания светодиода.
 *
 * Реализует алгоритм периодического включения/выключения светодиода
 * на основе счётчика таймера. Цикл мигания состоит из двух фаз:
 * - включение на BLINK_TIME единиц счётчика;
 * - выключение на BLINK_TIME единиц счётчика.
 *
 * @details
 * Функция использует статические переменные для хранения состояния:
 * - blink_status: текущий этап мигания (0 — выключен, 1 — включён);
 * - blink_count: значение счётчика при последнем переключении состояния.
 *
 * Переключение происходит только после истечения заданного интервала
 * (BLINK_TIME) относительно последнего изменения состояния.
 *
 * @note
 * - Должна регулярно вызываться из основного цикла программы.
 * - Для работы требуется корректная реализация get_timer_count().
 * - LED_INSTANCE должен быть валидным экземпляром sl_led_t.
 * - Начальное состояние: светодиод выключен (blink_status = 0).
 *
 * @see get_timer_count()
 */
void blink_process_action(void);

#endif // BLINK_H
